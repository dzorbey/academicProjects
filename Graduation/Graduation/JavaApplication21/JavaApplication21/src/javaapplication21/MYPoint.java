
package javaapplication21;


public class MYPoint {
   public int x;
   public int y;
    
    public MYPoint() {
    this.x=10;
    this.y=10;
    }
    
    public void setX(int temp) {
        this.x=temp;
    }
    
    
    public void setY(int temp2) {
        this.y=temp2;
    }
    
    
    public int getX() {
        return this.x;
    }
    
    
    public int getY() {
        return this.y;
    }
    
    
    
}

